<h2>ویژگی‌ها</h2>
</div>
<div class="content description">
<p><strong>&#8211; نت برگ آنی: </strong>امکان استفاده، یک لحظه پس از خرید تا پایان زمان استفاده</p>
<p><strong>&#8211; لیزر موهای زائد با دستگاه الکساندرایت Noblex 2017<br />
</strong></p>
<p><strong>&#8211; لیزر الکساندرایت در مطب دکتر همراه با ماساژ صورت رایگان</strong></p>
<p>&#8211; آپدیت نرم افزاری دستگاه الکساندرایت 2017</p>
<p>&#8211; در صورت خرید کل بدن، هزینه آژانس بازگشت به عهده مطب می باشد</p>
<p>&#8211; در صورت خرید بیش از 10 نت برگ، هزینه پارکینگ مجموعه رایگان می باشد</p>
<p>&#8211; از پیشرفته ترین دستگاههای لیزر دنیا با آخرین تکنولوژی دنیا</p>
<p>&#8211; سریعترین، جدیدترین، موثرترین و بی خطرترین دستگاه لیزر در حال حاضر در دنیا</p>
<p>&#8211; دارای تائیدیه FDA آمریکا</p>
<p>&#8211; موثر، بدون درد، سوزش و سوختگی</p>
<div>
<div>&#8211; لیزر الکساندرایت با قابلیت تنظیم طول موج و مناسب برای انواع پوست ها حتی تیره و برنزه و آفتاب گرفته و موهای کرکی و نازک</div>
<div></div>
<div>&#8211; دکتر اردستانی با سابقه درخشان در زمینه خدمات زیبایی</div>
<div></div>
<div>&#8211; لیزر الکساندرایت Fractional موجود در بازار با جدیدترین متد whitening (روشن کنندگی نواحی تیره)</div>
<div></div>
<div>&#8211; دستیابی به نتیجه ایده آل در حداقل تعداد جلسات و با بالاترین سرعت ممکن</div>
</div>
<p>&#8211; لیزر الکساندرایت دارای سیستم کولینگ(خنک کننده) قوی و استفاده همزمان کولینگ باد سرد قوی و گاز خنک کننده با بیشترین رضایت مشتریان</p>
<p>&#8211; خنک کننده هوای Zimmer آلمان</p>
<p>&#8211; بدون نیاز به استفاده از ژل در حین لیزر</p>
<p>&#8211; لیزر الکساندرایت دارای بزرگترین سطح منطقه لیزر شده</p>
<p>&#8211; نتیجه گیری در حداقل تعداد جلسات(حتی در اولین جلسه مشهود است)</p>
<p>&#8211; طول موج های لیزر الکساندرایت بیشترین مقدار تاثیر در انواع پوست ها و پوست های ایرانی</p>
